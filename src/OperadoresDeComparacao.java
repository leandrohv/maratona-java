public class OperadoresDeComparacao {
    public static void main(String[] args) {
        boolean dezMaiorQueVinte = 10 > 20;
        boolean dezMaiorIgualaVinte = 10 >= 20;
        boolean dezMenorQueVinte = 10 < 20;
        boolean dezMenorIgualAVinte = 10 <= 20;

        System.out.println("Dez maior que vinte " + dezMaiorQueVinte);
        System.out.println("Dez maior igual a vinte " + dezMaiorIgualaVinte);
        System.out.println("Dez menor que vinte " + dezMenorQueVinte);
        System.out.println("Dez menor igual a vinte " + dezMenorIgualAVinte);
    }
}
